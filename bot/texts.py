def tone_button_label(tone: dict) -> str:
    """Подпись кнопки оттенка вида «🙂 Приятное»."""
    return f"{tone['emoji']} {tone['name'].capitalize()}"


def emotion_label(emotion: dict) -> str:
    """Возвращает подпись вида «😟 Тревога» для кнопки и записей."""
    return f"{emotion['emoji']} {emotion['name']}"


def build_emotions_help() -> str:
    """Собирает текст словарика для команды /emotions."""
    lines = [
        "📖 <b>Словарик чувств</b>",
        "Если сложно подобрать название тому, что внутри — вот короткие ориентиры.",
        "",
    ]
    for emotion in EMOTIONS:
        lines.append(f"{emotion['emoji']} <b>{emotion['name']}</b> — {emotion['description']}")
    return "\n".join(lines)


